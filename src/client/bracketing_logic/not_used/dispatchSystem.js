let submitResult = (matchNumber, winner, loser, score) => {
  for (let x = 0; x < fullMatchArray.length; x++) {
    if (fullMatchArray[x].matchNumber === matchNumber) {
      fullMatchArray[x].winner = winner;
      fullMatchArray[x].loser = loser;
      fullMatchArray[x].score = score;
      fullMatchArray[x].dispatched = false;

      //This will update the matches that are dependent upon the result:
      for (let y = 0; y < fullMatchArray.length; y++) {
        //I'm a little hesitant on my slopiness of the string concatenation below (relying on JS to coerce the number into a string). Hopefully it doesn't come back to haunt me.
        if (
          fullMatchArray[y].topLineWrestler ===
          "winnerOfBout#" + matchNumber
        ) {
          fullMatchArray[y].topLineWrestler = winner;
          fullMatchArray[y].topLineWrestlersPreviousResult = score;
        }
        if (
          fullMatchArray[y].topLineWrestler ===
          "loserOfBout#" + matchNumber
        ) {
          fullMatchArray[y].topLineWrestler = loser;
          fullMatchArray[y].topLineWrestlersPreviousResult = score;
        }
        if (
          fullMatchArray[y].bottomLineWrestler ===
          "winnerOfBout#" + matchNumber
        ) {
          fullMatchArray[y].bottomLineWrestler = winner;
          fullMatchArray[y].bottomLineWrestlersPreviousResult = score;
        }
        if (
          fullMatchArray[y].bottomLineWrestler ===
          "loserOfBout#" + matchNumber
        ) {
          fullMatchArray[y].bottomLineWrestler = loser;
          fullMatchArray[y].bottomLineWrestlersPreviousResult = score;
        }
      }
    }
  }
};

