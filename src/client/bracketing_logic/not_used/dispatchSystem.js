//With this system we will have to manuualy submit each match to a mat, but we will always have unlimited digital mats.
let dispatch = (matchNumber, matNumber) => {
  for (let x = 0; x < fullMatchArray.length; x++) {
    if (matchNumber === fullMatchArray[x].matchNumber) {
      fullMatchArray[x].dispatched = true;
      fullMatchArray[x].dispatchedToMat = matNumber;
    }
  }
};

